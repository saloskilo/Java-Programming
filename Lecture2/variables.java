package Lecture2;

public class variables {
    public static void main(String[] args) {

        byte exampleByte = 127;
        short exampleShort = 32000;
        int exampleInt = 2147483647;
        long exampleLong = 9223372036854775807L;

        // Floating-point types
        float exampleFloat = 3.14f;
        double exampleDouble = 3.141592653589793;

        // Character type
        char exampleChar = 'A';

        // Boolean type
        boolean exampleBoolean = true;

        // Print examples of each data type
        System.out.println("Byte example: " + exampleByte);
        System.out.println("Short example: " + exampleShort);
        System.out.println("Int example: " + exampleInt);
        System.out.println("Long example: " + exampleLong);
        System.out.println("Float example: " + exampleFloat);
        System.out.println("Double example: " + exampleDouble);
        System.out.println("Char example: " + exampleChar);
        System.out.println("Boolean example: " + exampleBoolean+"\n");
    
    }
}
